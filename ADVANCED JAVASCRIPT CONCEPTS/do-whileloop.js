// DO-WHILE LOOP
let text = ""
let i = 0;

do {
  text += "The number is " + i + "\n";
  i++;
}
while (i < 10); 
console.log(text);

// output
// The number is 0
// The number is 1
// The number is 2
// The number is 3
// The number is 4
